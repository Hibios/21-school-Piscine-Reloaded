int		ft_iterative_factorial(int nb)
{
	int sum;

	sum = 1;
	if ((nb < 0) || (nb > 12))
		return (0);
	if ((nb == 1) || (nb == 0))
		return (1);
	while (nb > 1)
	{
		sum *= nb;
		nb--;
	}
	return (sum);
}
